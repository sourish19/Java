public class data_types {
    static void main(String[] args) {
        int val = 12;
        boolean val2; // by, default it is false
        byte val3 = 13; // can store very short number usually -127 - 127
        char val4 = 'j';
        double val5 = 3.14;
        float val6 = 4.3f; // need to add f literal to denote java that it is not double it will be float otherwise java will consider it as double
//        string val7 = "wqwqwq";
        byte val7 = (byte)val; // this is explicit type conversion
    }
}
